public class Tema4_Ejercicio_20 {
    public static void main(String[] args) {
        // Introducción
        System.out.println("Múltiplos de 5 entre 1 y 100:");
        // Desarrollo
        for (int i = 1; i <= 100; i++) {
            if (i % 5 == 0) {
                System.out.print(i + " ");
            }
        }
        // Desenlace
        System.out.println();
    }
}
